pub fn is_wall_at(x: f32, y: f32) -> bool {
    if x < 0.0 || x >= MAP_WIDTH as f32 || y < 0.0 || y >= MAP_HEIGHT as f32 {
        return true; // Считаем, что за пределами карты - стены, чтобы избежать выхода из границ.
    }
    WORLD_MAP[y as usize][x as usize] == 1
}

pub fn render_frame_sdl(canvas: &mut Canvas<Window>, player: &Player) -> Result<(), String> {
    canvas.set_draw_color(Color::RGB(0, 0, 0)); // Цвет фона (небо)
    canvas.clear();
    canvas.set_draw_color(Color::RGB(50, 50, 50)); // Цвет пола

    // Рисуем пол
    canvas.fill_rect(sdl2::rect::Rect::new(0, (SCREEN_HEIGHT / 2) as i32, SCREEN_WIDTH as u32, (SCREEN_HEIGHT / 2) as u32))?;

    // Максимальная дистанция видимости и коэффициент затухания света
    let max_view_distance = 20.0;
    let light_decay = 0.1; // Коэффициент затухания света

    for x in 0..SCREEN_WIDTH {
        // Для каждого столбца экрана вычисляем направление луча
        let ray_angle = player.direction - FOV / 2.0 + (x as f32 / SCREEN_WIDTH as f32) * FOV;
        let mut distance_to_wall = 0.0;

        let eye_x = ray_angle.cos();
        let eye_y = ray_angle.sin();

        let mut hit_wall = false;

        while !hit_wall && distance_to_wall < max_view_distance {
            distance_to_wall += 0.01;

            let test_x = (player.x + eye_x * distance_to_wall) as isize;
            let test_y = (player.y + eye_y * distance_to_wall) as isize;

            // Проверяем, не вышел ли луч за пределы карты.
            if test_x < 0 || test_x >= MAP_WIDTH as isize || test_y < 0 || test_y >= MAP_HEIGHT as isize || is_wall_at(test_x as f32, test_y as f32) {
                hit_wall = true;
            }
        }

        // Вычисляем расстояние до потолка и пола.
        let correct_distance = distance_to_wall * (ray_angle - player.direction).cos();
        
        let ceiling = (SCREEN_HEIGHT as f32 / 2.0) - SCREEN_HEIGHT as f32 / correct_distance;
        let floor = SCREEN_HEIGHT as f32 - ceiling;

        // Имитация освещения с учетом максимальной дистанции видимости
        let light_level = if distance_to_wall < max_view_distance {
            1.0 / (1.0 + distance_to_wall * distance_to_wall * light_decay + (ray_angle - player.direction).abs() * 0.2)
        } else {
            0.0 // Вне максимальной дистанции видимости уровень освещенности равен 0
        };
        let wall_color = (255.0 * light_level) as u8;

        let color = Color::RGB(wall_color, wall_color, wall_color); // Используем серый цвет для стен с учетом освещения

        canvas.set_draw_color(color);
        canvas.draw_line(Point::new(x as i32, ceiling as i32), Point::new(x as i32, floor as i32))?;
    }
    
    minimap::draw_mini_map(canvas, player, &WORLD_MAP)?;
    
    canvas.present();
    Ok(())
}